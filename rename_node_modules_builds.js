const fs = require('fs');
const path = require('path');

const nodeModulesPath = path.join(__dirname, 'node_modules');

function renameBuildFolders(dir) {
    if (!fs.existsSync(dir)) return;

    const files = fs.readdirSync(dir);
    for (const file of files) {
        const filePath = path.join(dir, file);
        try {
            const stats = fs.statSync(filePath);
            if (stats.isDirectory()) {
                if (file === 'android') {
                    const buildPath = path.join(filePath, 'build');
                    if (fs.existsSync(buildPath)) {
                        const random = Math.floor(Math.random() * 100000);
                        const newPath = path.join(filePath, `build_old_${random}`);
                        try {
                            fs.renameSync(buildPath, newPath);
                            console.log(`Renamed: ${buildPath} -> ${newPath}`);
                        } catch (err) {
                            console.log(`Failed to rename ${buildPath}:`, err.message);
                        }
                    }
                } else {
                    renameBuildFolders(filePath);
                }
            }
        } catch (err) {
            // Ignore stats errors (e.g. symlinks)
        }
    }
}

if (fs.existsSync(nodeModulesPath)) {
    console.log('Scanning node_modules for android/build folders...');
    renameBuildFolders(nodeModulesPath);
    console.log('Finished scanning.');
} else {
    console.log('node_modules folder not found.');
}
